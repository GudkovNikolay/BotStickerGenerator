    @classmethod
    def get_random_emoji(cls, category=None):
        """Получить случайный эмодзи из указанной категории или из всех"""
        if category and category in cls.EMOJI_CATEGORIES:
            return random.choice(cls.EMOJI_CATEGORIES[category])
        
        # Если категория не указана, выбираем из всех категорий
        all_emojis = []
        for cat_emojis in cls.EMOJI_CATEGORIES.values():
            all_emojis.extend(cat_emojis)
        return random.choice(all_emojis)
    
    @classmethod
    def get_emoji_categories(cls):
        """Получить список категорий"""
        return list(cls.EMOJI_CATEGORIES.keys())
    
    @classmethod
    def get_emojis_by_category(cls, category, limit=16):
        """Получить эмодзи из категории (с ограничением)"""
        if category not in cls.EMOJI_CATEGORIES:
            return []
        return cls.EMOJI_CATEGORIES[category][:limit]
    
    @classmethod
    def is_valid_emoji(cls, emoji_char):
        """Проверяет, является ли символ эмодзи"""
        # Простая проверка: эмодзи обычно имеют длину больше 1 в некоторых случаях
        # или находятся в определенных диапазонах Unicode
        import unicodedata
        if not emoji_char:
            return False
        # Проверяем, что это не обычный текст
        return any(category == 'So' for category in unicodedata.name(emoji_char, '').split())